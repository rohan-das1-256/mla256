public class Exp8 {
    public static void main(String[] args) {
        //autoboxing
        int x = 30;
        Integer xx = new Integer(x);
        System.out.println(xx);

        //auto-unboxing --> wrapper class to primitive class

        Long y = 6756l;
        long yy = y;
        System.out.println(yy);
    }
}
